function generateUiStateTests(screens: string[]): string {
  const tests: string[] = [
    `// Generated by discipline:test-scaffold - UI state tests`,
    `// Verify that each screen handles all 4 required states`,
    ``,
    `import { describe, it, expect } from '@jest/globals';`,
    ``,
    `describe('UI States', () => {`,
  ];

  for (const screen of screens) {
    tests.push(`  describe('${screen}', () => {`);
    for (const state of ['loading', 'empty', 'error', 'normal']) {
      tests.push(`    it('renders ${state} state correctly', async () => {`);
      tests.push(`      // TODO: Mount component with ${state} props/state`);
      tests.push(`      // TODO: Verify expected elements are visible`);
      tests.push(`    });`);
      tests.push(``);
    }
    tests.push(`  });`);
    tests.push(``);
  }

  tests.push(`});`);
  return tests.join('\n');
}

// Main
function main() {
  const disciplineContent = readIfExists(path.join(projectRoot, 'discipline.md'));
  if (!disciplineContent) {
    disciplineError('discipline.md not found. Run discipline:hydrate first.');
    process.exit(1);
  }

  let config;
  try { config = readDisciplineConfig(projectRoot); } catch {
    disciplineError('Could not read discipline.md config.');
    process.exit(1);
  }

  if (!fs.existsSync(outputDir)) fs.mkdirSync(outputDir, { recursive: true });

  const entities = extractEntities(disciplineContent);
  const endpoints = extractApiEndpoints(disciplineContent);
  const screens = extractScreens(path.join(projectRoot, '.discipline', 'packets'));

  let filesCreated = 0;

  // Backend adapter tests (always)
  const backendTests = generateBackendTests(entities, config);
  fs.writeFileSync(path.join(outputDir, 'backend-contracts.test.ts'), backendTests);
  filesCreated++;
  disciplineInfo(`Created: tests/generated/backend-contracts.test.ts (${entities.length} entities)`);

  // RLS tests (if Supabase)
  if (config.backendProvider === 'SUPABASE') {
    const rlsTests = generateRlsTests(entities, config);
    fs.writeFileSync(path.join(outputDir, 'rls-policies.test.ts'), rlsTests);
    filesCreated++;
    disciplineInfo(`Created: tests/generated/rls-policies.test.ts (${entities.length + 3} tables)`);
  }

  // API tests (if API lane or endpoints exist)
  if (endpoints.length > 0) {
    const apiTests = generateApiTests(endpoints);
    fs.writeFileSync(path.join(outputDir, 'api-endpoints.test.ts'), apiTests);
    filesCreated++;
    disciplineInfo(`Created: tests/generated/api-endpoints.test.ts (${endpoints.length} endpoints)`);
  }

  // UI state tests (if screens exist)
  if (screens.length > 0) {
    const uiTests = generateUiStateTests(screens);
    fs.writeFileSync(path.join(outputDir, 'ui-states.test.ts'), uiTests);
    filesCreated++;
    disciplineInfo(`Created: tests/generated/ui-states.test.ts (${screens.length} screens x 4 states)`);
  }

  console.log(`\n${filesCreated} test file(s) scaffolded in ${path.relative(projectRoot, outputDir)}/`);
  console.log('Each test has TODO markers. Fill in the implementation details.');
}

main();
