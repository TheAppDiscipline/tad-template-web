/**
 * discipline:test-scaffold
 *
 * Reads discipline.md contracts (data model, API shapes, sync rules)
 * and generates test stubs: 1 happy path + 1 error path per contract.
 *
 * Usage:
 *   npm run discipline:test-scaffold
 *   npm run discipline:test-scaffold -- --output tests/generated/
 *
 * Generates:
 * - Backend adapter tests (ensurePersonalSpace, listNotifications, etc.)
 * - RLS policy tests if BACKEND=SUPABASE
 * - API endpoint tests if LANE=BACKEND
 * - UI state tests for each screen in UI_HANDOFF_PACKET
 */

import * as fs from 'node:fs';
import * as path from 'node:path';
import minimist from 'minimist';
import { disciplineInfo, disciplineError } from './lib/types.js';
import { resolveProjectRoot, readDisciplineConfig } from './lib/discipline-config.js';

const args = minimist(process.argv.slice(2));
const projectRoot = resolveProjectRoot(args['project-dir']);
const outputDir = args.output || path.join(projectRoot, 'tests', 'generated');

function readIfExists(filePath: string): string | null {
  return fs.existsSync(filePath) ? fs.readFileSync(filePath, 'utf-8') : null;
}

function extractEntities(disciplineContent: string): string[] {
  const dataModelSection = disciplineContent.match(/## 3\) Data Model([\s\S]*?)## 4\)/);
  if (!dataModelSection) return [];

  const entities: string[] = [];
  const matches = dataModelSection[1].matchAll(/(?:table|entity|collection|type)\s*[:`]\s*(\w+)/gi);
  for (const m of matches) {
    if (!['N/A', 'undefined', 'null'].includes(m[1])) entities.push(m[1]);
  }

  // Also extract from markdown headers like "### spaces" or "### items"
  const headerMatches = dataModelSection[1].matchAll(/###\s+(\w+)/g);
  for (const m of headerMatches) {
    if (!entities.includes(m[1]) && m[1].length > 2) entities.push(m[1]);
  }

  return [...new Set(entities)];
}

function extractApiEndpoints(disciplineContent: string): Array<{ method: string; path: string; description: string }> {
  const apiSection = disciplineContent.match(/## 4\) API \/ IO Shapes([\s\S]*?)## 5\)/);
  if (!apiSection) return [];

  const endpoints: Array<{ method: string; path: string; description: string }> = [];
  const matches = apiSection[1].matchAll(/(GET|POST|PUT|PATCH|DELETE)\s+([/\w{}:-]+)\s*[-:]?\s*(.*)/gi);
  for (const m of matches) {
    endpoints.push({ method: m[1].toUpperCase(), path: m[2], description: m[3].trim() });
  }
  return endpoints;
}

function extractScreens(packetsDir: string): string[] {
  const uiPacket = readIfExists(path.join(packetsDir, 'UI_HANDOFF_PACKET.md'));
  if (!uiPacket) return [];

  const screens: string[] = [];
  const matches = uiPacket.matchAll(/##\s+SCREEN:\s*(.+)/gi);
  for (const m of matches) screens.push(m[1].trim());
  return screens;
}

